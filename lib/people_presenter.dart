  List<People> search(String search) {
    List<People> list = PeoplePresent().getPostList();
    List<People> peoples = [];
    for (var item in list) {
      if (item.name.toString().toLowerCase().contains(search.toLowerCase())) {
        peoples.add(item);
      }
    }
    return peoples;
  }
